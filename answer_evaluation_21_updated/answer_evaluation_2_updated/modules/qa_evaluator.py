import logging
from typing import Dict, List, Any
from nltk.tokenize import sent_tokenize, word_tokenize
from nltk.corpus import stopwords

logger = logging.getLogger(__name__)

class QAEvaluator:
    
    def __init__(self):
        try:
            self.stop_words = set(stopwords.words('english'))
        except:
            self.stop_words = set()
            
    def analyze_completeness(self, student_text: str, reference_text: str) -> Dict[str, Any]:
        """
        Assess completeness of the student answer relative to the reference.

        The reference (model answer) is generated by an LLM and is typically
        2–4x longer than a real student answer. We adjust for this by:
        - Using 35% of reference length as the 100%-completion target (not 60%).
        - Weighting content-word overlap more heavily than raw length.
        """
        student_words   = len(word_tokenize(student_text))
        reference_words = len(word_tokenize(reference_text))
        
        # 35% of model-answer length = full marks for length
        length_ratio = min(student_words / (reference_words * 0.35), 1.0) if reference_words > 0 else 0.0
        
        # Content coverage: meaningful word overlap
        student_content   = set(w.lower() for w in word_tokenize(student_text)
                                if w.isalpha() and w.lower() not in self.stop_words)
        reference_content = set(w.lower() for w in word_tokenize(reference_text)
                                if w.isalpha() and w.lower() not in self.stop_words)
        
        content_coverage = (len(student_content & reference_content) / len(reference_content)
                            if reference_content else 0.0)
        
        # Sentence count ratio (capped at 1)
        student_sentences   = len(sent_tokenize(student_text))
        reference_sentences = len(sent_tokenize(reference_text))
        sentence_ratio = min(student_sentences / reference_sentences, 1.0) if reference_sentences > 0 else 0.0
        
        # Weighted score — content overlap matters most
        completeness_score = (length_ratio * 0.30 + content_coverage * 0.50 + sentence_ratio * 0.20) * 100
        
        return {
            'score': min(completeness_score, 100.0),
            'length_ratio': length_ratio,
            'content_coverage': content_coverage,
            'sentence_ratio': sentence_ratio,
            'student_word_count': student_words,
            'reference_word_count': reference_words,
            'student_sentence_count': student_sentences,
            'reference_sentence_count': reference_sentences,
            'unique_content_words': len(student_content),
            'covered_content_words': len(student_content & reference_content)
        }
        
    def evaluate_answer_quality(self, student_text: str, question: str, reference_answer: str) -> Dict[str, Any]:
        completeness = self.analyze_completeness(student_text, reference_answer)
        question_keywords = self._extract_question_keywords(question)
        answer_coverage   = self._analyze_question_coverage(student_text, question_keywords)
        structure_analysis = self._analyze_answer_structure(student_text)
        quality_score = (
            completeness['score']       * 0.4 +
            answer_coverage['score']    * 0.4 +
            structure_analysis['score'] * 0.2
        )
        return {
            'quality_score': min(quality_score, 100.0),
            'completeness': completeness,
            'question_coverage': answer_coverage,
            'structure': structure_analysis,
            'feedback': self._generate_qa_feedback(completeness, answer_coverage, structure_analysis)
        }
        
    def _extract_question_keywords(self, question: str) -> List[str]:
        question_words = {'what','how','why','when','where','which','who',
                          'explain','describe','compare','discuss'}
        words = word_tokenize(question.lower())
        return [w for w in words if w.isalpha() and w not in self.stop_words
                and w not in question_words and len(w) > 2]
        
    def _analyze_question_coverage(self, student_text: str, question_keywords: List[str]) -> Dict[str, Any]:
        if not question_keywords:
            return {'score': 100.0, 'covered_keywords': [], 'missing_keywords': []}
        student_lower    = student_text.lower()
        covered_keywords = [kw for kw in question_keywords if kw in student_lower]
        missing_keywords = [kw for kw in question_keywords if kw not in student_lower]
        coverage_ratio   = len(covered_keywords) / len(question_keywords)
        return {
            'score':            coverage_ratio * 100,
            'covered_keywords': covered_keywords,
            'missing_keywords': missing_keywords,
            'coverage_ratio':   coverage_ratio
        }
        
    def _analyze_answer_structure(self, student_text: str) -> Dict[str, Any]:
        sentences      = sent_tokenize(student_text)
        words          = word_tokenize(student_text)
        avg_sent_len   = len(words) / len(sentences) if sentences else 0
        structure_score = 0
        if len(sentences) >= 3:
            structure_score += 40
        if 10 <= avg_sent_len <= 25:
            structure_score += 30
        if len(words) >= 50:
            structure_score += 30
        return {
            'score':            min(structure_score, 100.0),
            'sentence_count':   len(sentences),
            'avg_sentence_len': avg_sent_len,
            'word_count':       len(words)
        }
        
    def _generate_qa_feedback(self, completeness, answer_coverage, structure) -> Dict[str, List[str]]:
        feedback = {'strengths': [], 'weaknesses': [], 'suggestions': []}
        if completeness['score'] > 80:
            feedback['strengths'].append("Good coverage of the topic")
        elif completeness['score'] < 50:
            feedback['weaknesses'].append("Answer lacks sufficient detail")
            feedback['suggestions'].append("Expand your answer with more key points")
        if answer_coverage.get('missing_keywords'):
            feedback['suggestions'].append(
                f"Address these aspects: {', '.join(answer_coverage['missing_keywords'][:3])}")
        if structure['score'] > 70:
            feedback['strengths'].append("Well-structured response")
        return feedback